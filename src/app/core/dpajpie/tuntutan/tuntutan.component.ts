  getChartTuntutan1() {
    /* Chart code */
    // Themes begin
    am4core.useTheme(am4themes_kelly);
    am4core.useTheme(am4themes_animated);
    // Themes end

    // Create chart instance
    let chart = am4core.create("tuntutan1", am4charts.XYChart3D);

    // Add data
    chart.data = [
      {
        country: "Asnaf",
        year2017: 5.5,
        year2018: 7.2,
      },
      {
        country: "GPAM",
        year2017: 3.7,
        year2018: 5.1,
      },
      {
        country: "Urusetia",
        year2017: 4.8,
        year2018: 5.9,
      },
      {
        country: "Pihak Ketiga",
        year2017: 5.6,
        year2018: 8.3,
      },
    ];

    // Create axes
    let categoryAxis = chart.xAxes.push(new am4charts.CategoryAxis());
    categoryAxis.dataFields.category = "country";
    categoryAxis.renderer.grid.template.location = 0;
    categoryAxis.renderer.minGridDistance = 30;

    let valueAxis = chart.yAxes.push(new am4charts.ValueAxis());
    valueAxis.title.text = "GDP growth rate";
    valueAxis.renderer.labels.template.adapter.add("text", function (text) {
      return text + "%";
    });

    // Create series
    let series = chart.series.push(new am4charts.ColumnSeries3D());
    series.dataFields.valueY = "year2017";
    series.dataFields.categoryX = "country";
    series.name = "Tuntutan";
    series.clustered = false;
    series.columns.template.tooltipText =
      "Tuntutan {country} : [bold]{valueY}[/]";
    series.columns.template.fillOpacity = 0.9;

    let series2 = chart.series.push(new am4charts.ColumnSeries3D());
    series2.dataFields.valueY = "year2018";
    series2.dataFields.categoryX = "country";
    series2.name = "Dana";
    series2.clustered = false;
    series2.columns.template.tooltipText =
      "Dana {country} : [bold]{valueY}[/]";

    this.chart1 = chart;
  }

